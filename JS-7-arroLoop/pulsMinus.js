const n = 10;
let string = ''
const pluses = 3;
 

// თქვენი ამოცანაა ააწყოთ სტრინგი სადაც n რაოდენობის მინუსის(-) სიმბოლოს ჩაწერთ.
// მაგალითად თუ: n = 10;
// ეკრანზე უნდა გამოიტანოთ: ----------

let result = '';


for (let i = 0; i < n; i++) {
    if (i < pluses) {
        result += '+';
    } else {
        result += '-';
    }
}

