// 1.  ფუნქციას სახელად dropCash() -  რომელსაც გადავცემ   პარამეტრს :  bills
// 2.  change -  ში ვინახავ  იმას თუ რა თანხა უნდა დავუბრუნოთ მომხმარებელს. 
// 3. ფორ ციკლით გადავირბენ bills -  ზე და მივწვდები ობიექტს. 

console.log(bills);



function dropCash(bills) {
    let change =  incomingBill - productPrice;

    for (let i = bills.length - 1; i >= 0; i--) {
        // console.log(bills[i]);
        if(change >=  bills[i].lari){
            change -= bills[i].lari; 
            bills[i].quantity--
            i++
        }
        if(change == 0) break
       console.log(change);
       
    }
   return change

}
