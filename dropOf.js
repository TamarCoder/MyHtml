// ამისათვის მან კუპიურა შეაცურა აპარატში:
const incomingBill = 50;

// ხოლო შეძენილი პროდუქტის ღირებულება იყო:
const productPrice = 5;

// აპარატში ხელმისაწვდომი კუპიურების რაოდენობა კი წერია ობიექტების მასივში:
